#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{	
	size_t			i;
	unsigned char	*dst_ptr;
	unsigned char	*src_ptr;

	if (!n)
		return (dst);
	if (!dst || !src)
		return (NULL);
	dst_ptr = (unsigned char *)dst;
	src_ptr = (unsigned char *)src;
	i = 0;
	while (i < n)
	{
		dst_ptr[i] = src_ptr[i];
		i++;
	}
	return (dst);
}
